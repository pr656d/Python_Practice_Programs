

# WAP to perform arithmetic operations
num1, num2 = int(input("num1: ")), int(input("num2: "))
print(str(num1), "+", str(num2), "=", str(num1+num2), "\n" +
      str(num1), "-", str(num2), "=", str(num1-num2), "\n" +
      str(num1), "*", str(num2), "=", str(num1*num2), "\n" +
      str(num1), "/", str(num2), "=", str(num1/num2),"\n" +
      str(num1), "%", str(num2), "=", str(num1%num2), "\n")
