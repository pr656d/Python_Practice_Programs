def dailyForcast(name):
    if name == 'sunday':
        print("Little cloudy")
    elif name == 'monday':
        print("Sunny")
    elif name == 'tuesday':
        print("Cloudy")
    elif name == 'wednesday':
        print("Rainy")
    elif name == 'thursday':
        print("Heavy Rain")
    elif name == 'friday':
        print("Clear")
    elif name == 'saturday':
        print("Hot")